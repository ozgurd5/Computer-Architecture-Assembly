.MODEL SMALL
.STACK 100H

.DATA
	password DB '2026'
	input DB 4 DUP(?)

	msgTamam DB 13,10,'TAMAM$'
	msgHata DB 13,10,'HATA$'

.CODE
MAIN PROC
	MOV AX, @DATA
	MOV DS, AX

	MOV CX, 4
	LEA SI, input

READ_LOOP:
	MOV AH, 08H		;read key WITHOUT echo
	INT 21H
	MOV [SI], AL	;store the real char
	MOV DL, '*'		;show a star instead
	MOV AH, 02H
	INT 21H
	INC SI
	LOOP READ_LOOP

	MOV CX, 4
	LEA SI, input
	LEA DI, password

COMPARE_LOOP:
	MOV AL, [SI]
	CMP AL, [DI]
	JNE WRONG

	INC SI
	INC DI
	LOOP COMPARE_LOOP

CORRECT:
	MOV AH, 09H
	LEA DX, msgTamam
	INT 21H
	JMP EXIT

WRONG:
	MOV AH, 09H
	LEA DX, msgHata
	INT 21H

EXIT:
	MOV AH, 4CH
	INT 21H

MAIN ENDP
END MAIN
